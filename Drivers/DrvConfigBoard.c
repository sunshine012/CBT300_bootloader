void DrvConfigBoardInit( void )
{
   CONFIGBOARD1_TRIS = TRIS_BIT_INPUT;
   CONFIGBOARD2_TRIS = TRIS_BIT_INPUT;
   CONFIGBOARD3_TRIS = TRIS_BIT_INPUT;

}

UINT8 DrvConfigBoardGet( void )
{
	UINT8 Temp;
	Temp =  0;
	
	if(CONFIGBOARD1_IO)
		Temp |= 0x01;
	if(CONFIGBOARD2_IO)
		Temp |= 0x02;
	if(CONFIGBOARD3_IO)
		Temp |= 0x04;
		
   	return Temp;
}

